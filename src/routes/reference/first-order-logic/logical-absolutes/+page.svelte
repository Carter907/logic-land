<h3>Principle of Bivalence</h3>
<p>
  This principle means that propositional variables have to be execlusively true
  or false. Your premise can not be true and false at the same time.
</p>
<h3 id="the-principle-of-non-contradiction">
  The principle of non-contradiction
</h3>
<p>
  this principle means that you must either assert or deny a claim, not both,
  not neither.
</p>

